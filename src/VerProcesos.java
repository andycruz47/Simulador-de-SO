        txtBuscar.addKeyListener(new KeyAdapter(){

            @Override
            public void keyReleased(KeyEvent ke){
                trs.setRowFilter(RowFilter.regexFilter(txtBuscar.getText(), 1));
            }
        });
        trs= new TableRowSorter(modelo);
        tbColaProcesos.setRowSorter(trs);
